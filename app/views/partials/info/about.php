    <div>
        <p><strong>My Clinic System by Luis Calero </strong> is an open-source software designed to manage the operations of a small clinic efficiently.</p>
        <p>It includes the following modules:</p>
        <ul>
            <li><strong>Home Dashboard</strong>: The main control panel that provides a general overview of the clinic’s activity and quick access to essential sections.</li>
            <li><strong>Patients</strong>: Module for registering, updating, and viewing patient records, including personal details and medical history.</li>
            <li><strong>Doctors</strong>: This module allows the administration and management of doctors’ profiles and specialties.</li>
            <li><strong>Appointments</strong>: Used to schedule, update, and monitor patient appointments with doctors.</li>
            <li><strong>Prescriptions</strong>: A module for creating and managing medical prescriptions issued by doctors for patients.</li>
            <li><strong>Invoices</strong>: The billing module, which helps generate and track invoices for the services provided.</li>
            <li><strong>Reports</strong>: Displays summarized information and statistical reports about the clinic’s activity, patients, and services.</li>
            <li><strong>User Manager</strong>: This administrative module enables user account management and access permissions across the system.</li>
        </ul>
        <p>This system is ideal for small clinics seeking a simple yet functional solution to streamline their daily operations.</p>

