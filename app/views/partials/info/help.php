    <div>
        <h5>1. What is My Clinic System?</h5>
        <p>My Clinic System is a free and open-source software designed to help small clinics manage appointments, patient records, billing, and more.</p>

        <h5>2. How can I add a new patient?</h5>
        <p>Go to the "Patients" module, click on "Add New Patient", and fill out the required personal and medical information.</p>

        <h5>3. Can I schedule appointments for multiple doctors?</h5>
        <p>Yes. The "Appointments" module allows you to assign patients to specific doctors and select available dates and times.</p>

        <h5>4. How do I create a medical prescription?</h5>
        <p>Navigate to the "Prescriptions" module, select the patient, and fill in the medication details prescribed by the doctor.</p>

        <h5>5. Is it possible to generate invoices for consultations?</h5>
        <p>Yes. The "Invoices" module allows you to create, manage, and download billing details for each appointment or treatment.</p>

        <h5>6. Who can manage user access?</h5>
        <p>Only users with administrator privileges can access the "User Manager" module to create, update, or remove users and assign roles.</p>

        <h5>7. Can I view clinic activity reports?</h5>
        <p>Yes. Go to the "Reports" section to view summaries of patients, appointments, income, and other key metrics.</p>

        <h5>8. How do I update my profile or change my password?</h5>
        <p>Click on your profile photo or username at the top right corner, then select "My Account" to update your details or reset your password.</p>

        <h5>9. Is the system accessible on mobile devices?</h5>
        <p>Yes. My Clinic System is responsive and can be accessed from smartphones and tablets through a web browser.</p>

        <h5>10. How can I back up the clinic data?</h5>
        <p>Database backup functionality must be configured manually through the server or hosting provider. It is recommended to do regular backups.</p>
